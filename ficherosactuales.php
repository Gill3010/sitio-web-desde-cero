// Función para mostrar el archivo según su tipo
function mostrarArchivo(archivo, extension) {
    var contenido = document.getElementById('contenidoArchivo');
    contenido.innerHTML = ''; // Limpiar contenido previo
    
    if (extension === 'txt') {
        // Mostrar archivo de texto
        fetch(archivo)
            .then(response => response.text())
            .then(data => {
                contenido.innerHTML = '<pre>' + data + '</pre>';
                document.getElementById('archivoModal').style.display = 'block';
            })
            .catch(error => console.error('Error al cargar el archivo:', error));
    } else if (extension === 'pdf') {
        // Mostrar archivo PDF
        contenido.innerHTML = '<iframe src="' + archivo + '" width="100%" height="600px"></iframe>';
        document.getElementById('archivoModal').style.display = 'block';
    } else if (extension === 'doc' || extension === 'docx') {
        // Mostrar archivo Word usando Google Docs Viewer
        var viewerUrl = 'https://docs.google.com/gview?url=' + encodeURIComponent(archivo) + '&embedded=true';
        contenido.innerHTML = '<iframe src="' + viewerUrl + '" width="100%" height="600px"></iframe>';
        document.getElementById('archivoModal').style.display = 'block';
    } else if (extension === 'pages') {
        // Opción de descarga para archivos .pages
        contenido.innerHTML = '<p>No se puede mostrar el archivo: ' + archivo + ' (formato no compatible). <a href="' + archivo + '" download>Descargar archivo</a></p>';
        document.getElementById('archivoModal').style.display = 'block';
    } else {
        // Mostrar mensaje de formato no compatible para otros tipos
        contenido.innerHTML = '<p>No se puede mostrar el archivo: ' + archivo + ' (formato no compatible)</p>';
        document.getElementById('archivoModal').style.display = 'block';
    }
}

// Función para mostrar la imagen en un modal
function abrirImagen(src) {
    var modal = document.getElementById('imagenModal');
    var img = document.getElementById('imagenMostrar');
    img.src = src;
    modal.style.display = 'block';
}

// Función para cerrar el modal de archivo
function cerrarModal() {
    document.getElementById('archivoModal').style.display = 'none';
}

// Función para cerrar el modal de imagen
function cerrarImagen() {
    document.getElementById('imagenModal').style.display = 'none';
}

// Función para reproducir el audio al hacer clic en el icono
function reproducirAudio(cardId, index, titulo) {
    detenerTodosLosAudios(); // Detener todos los audios antes de reproducir el nuevo
    
    var audio = document.getElementById('audio-' + cardId + '-' + index);
    var tituloContainer = document.getElementById('tituloAudio');
    var tituloTexto = document.getElementById('tituloAudio');

    if (audio.paused) {
        audio.play();
        tituloTexto.textContent = titulo; // Configura el texto del título
        tituloContainer.style.display = 'block'; // Muestra el contenedor del título
    } else {
        audio.pause();
        audio.currentTime = 0; // Reiniciar el audio si se pausa
        tituloContainer.style.display = 'none'; // Oculta el contenedor del título
    }

    // Evento para ocultar el título cuando el audio termina
        // Evento para ocultar el título cuando el audio termina
    audio.onended = function () {
        tituloContainer.style.display = 'none';
    };
}

// Función para eliminar un card
function eliminarCard(id) {
    if (confirm('¿Estás seguro de que quieres eliminar este cartel?')) {
        fetch('eliminar.php', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/x-www-form-urlencoded',
            },
            body: 'id=' + encodeURIComponent(id),
        })
        .then(response => response.text())
        .then(data => {
            if (data.trim() === 'success') {
                // Eliminar el card del DOM
                var card = document.querySelector(`.card[data-id="${id}"]`);
                if (card) {
                    card.parentElement.remove(); // Elimina el card del DOM
                }
            } else {
                alert('Error al eliminar el registro. Inténtalo de nuevo.');
            }
        })
        .catch(error => console.error('Error al eliminar el registro:', error));
    }
}

// Cerrar el modal y detener el audio cuando se presiona la tecla "Escape"
