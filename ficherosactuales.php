    if (event.key === "Escape") { // Verifica si la tecla presionada es "Escape"
        cerrarModal();
        cerrarImagen();
        detenerTodosLosAudios(); // Detener todos los audios cuando se presiona Escape
    }
});

// Función para detener todos los audios
function detenerTodosLosAudios() {
    var audios = document.querySelectorAll('audio');
    audios.forEach(function(audio) {
        audio.pause();
        audio.currentTime = 0; // Reiniciar el audio si se detiene
    });
}

